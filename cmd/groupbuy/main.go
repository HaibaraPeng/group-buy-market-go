package main

import (
	"flag"
	"fmt"
	"os"

	"github.com/go-kratos/kratos/v2"
	"github.com/go-kratos/kratos/v2/log"
	"github.com/go-kratos/kratos/v2/transport/http"
	"gorm.io/gorm"

	"group-buy-market-go/internal/conf"
	"group-buy-market-go/internal/domain"
	"group-buy-market-go/internal/domain/activity/service"
	"group-buy-market-go/internal/domain/activity/service/discount"
	"group-buy-market-go/internal/domain/activity/service/trial/node"
	"group-buy-market-go/internal/infrastructure/adapter/repository"
	"group-buy-market-go/internal/infrastructure/dao"
	"group-buy-market-go/internal/server"
)

// go build -ldflags "-X main.Version=x.y.z" .
var (
	// Name is the name of the compiled software.
	Name string
	// Version is the version of the compiled software.
	Version string
	// flagconf is the config flag.
	flagconf string

	id, _ = os.Hostname()
)

func init() {
	flag.StringVar(&flagconf, "conf", "configs/config.yaml", "config path, eg: -conf config.yaml")
}

func newApp(logger log.Logger, hs *http.Server) *kratos.App {
	return kratos.New(
		kratos.ID(id),
		kratos.Name(Name),
		kratos.Version(Version),
		kratos.Metadata(map[string]string{}),
		kratos.Logger(logger),
		kratos.Server(
			hs,
		),
	)
}

func main() {
	flag.Parse()

	// Create logger
	logger := log.With(log.NewStdLogger(os.Stdout),
		"ts", log.DefaultTimestamp,
		"caller", log.DefaultCaller,
		"service.id", id,
		"service.name", Name,
		"service.version", Version,
	)

	// Load configuration
	cfg, err := conf.LoadConfig(flagconf)
	if err != nil {
		logger.Log(log.LevelFatal, "msg", "Failed to load config", "error", err)
	}

	// Initialize database
	db, err := cfg.InitDB()
	if err != nil {
		logger.Log(log.LevelFatal, "msg", "Failed to initialize database", "error", err)
	}
	defer func() {
		sqlDB, _ := db.DB()
		sqlDB.Close()
	}()

	// Initialize server with Wire
	srv, err := initializeServer(db)
	if err != nil {
		logger.Log(log.LevelFatal, "msg", "Failed to initialize server", "error", err)
	}

	// Create HTTP server
	httpSrv := http.NewServer(http.Address(fmt.Sprintf("%s:%d", cfg.Server.Host, cfg.Server.Port)))

	// Register HTTP handlers using Kratos transport
	httpSrv.HandlePrefix("/", srv)

	app := newApp(logger, httpSrv)
	// start and wait for stop signal
	if err := app.Run(); err != nil {
		logger.Log(log.LevelFatal, "msg", "Application failed to run", "error", err)
	}
}

// Wire initialization function (will be generated by Wire)
func initializeServer(db *gorm.DB) (*server.Server, error) {
	mySQLGroupBuyActivityDAO := dao.NewMySQLGroupBuyActivityDAO(db)
	mySQLGroupBuyDiscountDAO := dao.NewMySQLGroupBuyDiscountDAO(db)
	mySQLSkuDAO := dao.NewMySQLSkuDAO(db)
	groupBuyService := domain.NewGroupBuyService(mySQLGroupBuyActivityDAO, mySQLGroupBuyDiscountDAO)

	// Create the market service that was missing
	activityRepository := repository.NewActivityRepository(mySQLGroupBuyActivityDAO, mySQLGroupBuyDiscountDAO, mySQLSkuDAO)

	// Create discount calculation services
	zjCalculateService := discount.NewZJCalculateService()
	zkCalculateService := discount.NewZKCalculateService()
	mjCalculateService := discount.NewMJCalculateService()
	nCalculateService := discount.NewNCalculateService()

	// Create the node hierarchy needed for market service
	endNode := node.NewEndNode()
	marketNode := node.NewMarketNode(endNode, activityRepository, zjCalculateService, zkCalculateService, mjCalculateService, nCalculateService)
	switchNode := node.NewSwitchNode(marketNode)
	rootNode := node.NewRootNode(switchNode)

	marketService := service.NewIIndexGroupBuyMarketService(rootNode)

	server := server.NewServer(mySQLGroupBuyActivityDAO, groupBuyService, marketService)
	return server, nil
}
