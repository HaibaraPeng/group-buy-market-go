package main

import (
	httpInterface "group-buy-market-go/internal/interfaces/http"
	"log"
	"net/http"
)

func main() {
	// Initialize server with Wire
	server := initializeServer()

	// Register routes
	server.Route("/", func(w http.ResponseWriter, r *http.Request) {
		w.Write([]byte("Hello, Group Buy Market!"))
	})

	server.Route("/test", func(w http.ResponseWriter, r *http.Request) {
		w.Write([]byte("Test endpoint is working!"))
	})

	log.Println("Server starting on :8080")
	// Start server
	err := http.ListenAndServe(":8080", server)
	if err != nil {
		log.Fatal(err)
	}
}

// Wire initialization function (will be generated by Wire)
func initializeServer() *httpInterface.Server {
	server := httpInterface.NewServer()
	return server
}
